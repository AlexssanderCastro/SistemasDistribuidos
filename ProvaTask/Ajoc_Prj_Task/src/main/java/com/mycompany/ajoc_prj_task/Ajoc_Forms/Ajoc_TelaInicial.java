    private void btnIniciarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIniciarActionPerformed
        int ajoc_porta = Integer.parseInt(ajoc_txtPorta.getText());
        int ajoc_tipo = ajoc_cmbTipo.getSelectedIndex();
        if(ajoc_tipo==0){
            this.ajoc_isMatriz = true;
        }
        this.ajoc_servidor = new Ajoc_ServidorSocketThread(ajoc_porta);
        ajoc_servidor.start();
    }//GEN-LAST:event_btnIniciarActionPerformed

