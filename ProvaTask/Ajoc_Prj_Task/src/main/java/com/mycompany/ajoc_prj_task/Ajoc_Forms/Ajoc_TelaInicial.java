    private void btnIniciar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIniciar1ActionPerformed
        try {
            String ajoc_IP = ajoc_txtIPB.getText();
            int ajoc_porta = Integer.parseInt(ajoc_txtPortaB.getText());
            this.ajoc_server = new Ajoc_serverHandler();
            ajoc_server.ajoc_conectar(ajoc_IP,ajoc_porta);
            this.ajoc_isCon=true;
            Ajoc_ArquivoHandler ajoc_handler = new Ajoc_ArquivoHandler();
            ajoc_handler.Ajoc_enviarSyncRegistroPorRegistro();
        } catch (IOException ex) {
            System.getLogger(Ajoc_TelaInicial.class.getName()).log(System.Logger.Level.ERROR, (String) null, ex);
        }

    }//GEN-LAST:event_btnIniciar1ActionPerformed

